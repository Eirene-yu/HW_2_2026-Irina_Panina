    models_dict = {}

    print(f"Seed: {random_states}")
    print(f"Параметры: epochs={epochs}, batch_size={batch_size}, max_len={max_len}")

    for seed_idx, rs in enumerate(random_states):
        print(f"\n{'#'*50}")
        print(f"Номер {seed_idx + 1}/{len(random_states)} | random_state = {rs}")
        print(f"{'#'*50}")

        # Разделение данных
        train_texts, test_texts, train_labels, test_labels = train_test_split(df['review'].values, df['label'].values, test_size=0.2, random_state=rs, stratify=df['label'])

