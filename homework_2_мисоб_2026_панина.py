        model = BertForSequenceClassification.from_pretrained('bert-base-uncased', num_labels=2, output_attentions=False, output_hidden_states=False).to(device)
        model_name = 'bert-base-uncased'

        # Инициализация оптимизатора
        optimizer = AdamW([
            {'params': model.bert.encoder.layer[-4:].parameters(), 'lr': 2e-5},
            {'params': model.classifier.parameters(), 'lr': 2e-5},
        ], lr=1e-5)

        # Инициализация scheduler
        total_steps = len(train_loader) * epochs
        scheduler = get_linear_schedule_with_warmup(
            optimizer,
            num_warmup_steps=int(0.1 * total_steps),
            num_training_steps=total_steps
        )

