def preprocess_russian_text(text, return_stats=False):
    """
    Функция для предобработки русскоязычного текста
    """

    morph = pymorphy3.MorphAnalyzer()

    russian_stopwords = set(stopwords.words("russian"))

    if return_stats:
        stats = {'initial_tokens': 0, 'after_stopwords': 0, 'after_lemmatization': 0}

    # Преобразование в нижний регистр
    text_lower = text.lower()

    # Удаление пунктуации и цифр
    text_clean = re.sub(r'[^\w\s]', ' ', text_lower)
    text_clean = re.sub(r'\d+', '', text_clean)
    text_clean = re.sub(r'\s+', ' ', text_clean).strip()

    # Токенизация
    tokens = text_clean.split()

    if return_stats:
        stats['initial_tokens'] = len(tokens)

    # Удаление стоп-слов и коротких слов
    tokens_no_stopwords = [token for token in tokens if token not in russian_stopwords and len(token) > 2]

    if return_stats:
        stats['after_stopwords'] = len(tokens_no_stopwords)

    # Лемматизация
    lemmatized_tokens = []

    for token in tokens_no_stopwords:
        try:
            parsed = morph.parse(token)[0]
            normal_form = parsed.normal_form
            lemmatized_tokens.append(normal_form)
        except:
            lemmatized_tokens.append(token)

    if return_stats:
        stats['after_lemmatization'] = len(lemmatized_tokens)
        stats['removed_percent'] = round((1 - len(lemmatized_tokens)/len(tokens)) * 100, 1)

    processed_text = ' '.join(lemmatized_tokens)

    if return_stats:
        return processed_text, stats
    else:
        return processed_text

def style_dataframe(df, gradient=False, num=0):
    """
    Функция для красивого отображения таблиц
    """

    header_color = '#2E7D32'
    index_color = '#2E7D32'
    hover_color = '#FFB74D'

    percent_cols = [col for col in df.columns if 'процентов' in str(col).lower() or '%' in str(col)]

    all_numeric = df.select_dtypes(include=[np.number]).columns.tolist()

    styles = [
        {'selector': 'thead th',
         'props': [('background-color', header_color), ('color', 'white'),
                   ('font-weight', 'bold'), ('text-align', 'center'), ('padding', '10px')]},
        {'selector': 'tbody th',
         'props': [('background-color', index_color), ('color', 'white'),
                   ('font-weight', 'bold'), ('text-align', 'left'), ('padding', '8px')]},
        {'selector': 'tbody td',
         'props': [('padding', '8px'), ('text-align', 'center'), ('font-size', '12px')]},
        {'selector': 'tbody tr:hover td, tbody tr:hover th',
         'props': [('background-color', hover_color), ('color', 'black'), ('transition', '0.2s')]}
    ]

    styled = df.style.set_table_styles(styles)

    format_dict = {}
    for col in all_numeric:
        if col in percent_cols:
            format_dict[col] = '{:.1f}%'
        else:
            format_dict[col] = f'{{:.{num}f}}'

    styled = styled.format(format_dict)

    if gradient and all_numeric:
        custom_cmap = LinearSegmentedColormap.from_list('custom', ['#FFF9C4', '#2E7D32'])
        styled = styled.background_gradient(cmap=custom_cmap, subset=all_numeric, axis=None)

    return styled

