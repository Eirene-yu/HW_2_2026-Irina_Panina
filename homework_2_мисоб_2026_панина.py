def train_epoch(model, data_loader, optimizer, scheduler, device, scaler, accumulation_steps=2, epoch_idx=0, grad_clip=1.0):
    model.train()
    losses = []
    correct_predictions = 0
    total_predictions = 0
    optimizer.zero_grad()

    batch_history = {
        'batch_steps': [],
        'batch_losses': [],
        'batch_accs': [],
        'learning_rates': [],
        'grad_norms': []
    }

    progress_bar = tqdm(data_loader, desc=f"Эпоха {epoch_idx}")

    for i, batch in enumerate(progress_bar):
        input_ids = batch['input_ids'].to(device, non_blocking=True)
        attention_mask = batch['attention_mask'].to(device, non_blocking=True)
        labels = batch['labels'].to(device, non_blocking=True)

        with autocast(dtype=torch.float16):
            outputs = model(input_ids=input_ids, attention_mask=attention_mask, labels=labels)
            loss = outputs.loss / accumulation_steps

        scaler.scale(loss).backward()

        if (i + 1) % accumulation_steps == 0:
            scaler.unscale_(optimizer)
            grad_norm = torch.nn.utils.clip_grad_norm_(model.parameters(), grad_clip)
            batch_history['grad_norms'].append(grad_norm.item())

            scaler.step(optimizer)
            scaler.update()
            scheduler.step()
            optimizer.zero_grad()

            batch_history['learning_rates'].append(optimizer.param_groups[0]['lr'])

        current_loss = loss.item() * accumulation_steps
        losses.append(current_loss)

        preds = torch.argmax(outputs.logits, dim=1)
        correct = torch.sum(preds == labels).item()
        batch_acc = correct / len(labels)
        correct_predictions += correct
        total_predictions += len(labels)

        current_epoch_acc = correct_predictions / total_predictions

        batch_history['batch_steps'].append(i)
        batch_history['batch_losses'].append(current_loss)
        batch_history['batch_accs'].append(batch_acc)

        progress_bar.set_postfix({
            'loss': f'{current_loss:.4f}',
            'avg_loss': f'{np.mean(losses):.4f}',
            'batch_acc': f'{batch_acc:.4f}',
            'epoch_acc': f'{current_epoch_acc:.4f}',
            'lr': f'{optimizer.param_groups[0]["lr"]:.2e}'
        })

    epoch_accuracy = correct_predictions / len(data_loader.dataset)

    return np.mean(losses), epoch_accuracy, batch_history

def eval_model(model, data_loader, device):
    model.eval()
    losses = []
    all_preds = []
    all_labels = []
    all_probs = []

    progress_bar = tqdm(data_loader, desc="Оценка")

    with torch.no_grad():
        for batch in progress_bar:
            input_ids = batch['input_ids'].to(device, non_blocking=True)
            attention_mask = batch['attention_mask'].to(device, non_blocking=True)
            labels = batch['labels'].to(device, non_blocking=True)

            with autocast(dtype=torch.float16):
                outputs = model(
                    input_ids=input_ids,
                    attention_mask=attention_mask,
                    labels=labels
                )

            loss = outputs.loss
            logits = outputs.logits
            probs = torch.softmax(logits, dim=1)

            losses.append(loss.item())
            preds = torch.argmax(logits, dim=1)

            all_preds.extend(preds.cpu().numpy())
            all_labels.extend(labels.cpu().numpy())
            all_probs.extend(probs.cpu().numpy())

            current_acc = accuracy_score(all_labels, all_preds)

            progress_bar.set_postfix({
                'loss': f'{loss.item():.4f}',
                'avg_loss': f'{np.mean(losses):.4f}',
                'acc': f'{current_acc:.4f}'
            })

    accuracy = accuracy_score(all_labels, all_preds)
    precision, recall, f1, _ = precision_recall_fscore_support(
        all_labels, all_preds, average='binary'
    )

    return np.mean(losses), accuracy, precision, recall, f1, all_preds, all_labels, all_probs

