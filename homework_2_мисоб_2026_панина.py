def measure_inference_time(model, test_loader, device, num_batches=10):
    """
    Измеряет время инференса модели
    """
    # Убеждаемся, что модель на правильном устройстве
    model = model.to(device)
    model.eval()
    times = []

    print(f"Измерение времени инференса на {num_batches} батчах...")
    print(f"Размер батча: {test_loader.batch_size}")
    print(f"Устройство: {device}")
    print("-" * 50)

    with torch.no_grad():
        for i, batch in enumerate(test_loader):
            if i >= num_batches:
                break

            # Перемещаем данные на то же устройство, что и модель
            input_ids = batch['input_ids'].to(device)
            attention_mask = batch['attention_mask'].to(device)

            if device.type == 'cuda':
                torch.cuda.synchronize()

            start_time = time.perf_counter()

            outputs = model(input_ids=input_ids, attention_mask=attention_mask)

            if device.type == 'cuda':
                torch.cuda.synchronize()

            end_time = time.perf_counter()

            batch_time = end_time - start_time
            times.append(batch_time)

            print(f"Батч {i+1}: {batch_time*1000:.2f} мс")

    # Статистика
    times_ms = np.array(times) * 1000
    avg_time_per_batch = np.mean(times)
    avg_time_per_sample = avg_time_per_batch / test_loader.batch_size
    std_time_per_batch = np.std(times)

